@dataclass
class WalkForwardResult:
    """Walk-forward validation result."""
    in_sample_sharpes: list[float]
    out_of_sample_sharpes: list[float]
    in_sample_returns: list[float]
    out_of_sample_returns: list[float]
    n_folds: int
    oos_sharpe: float          # average OOS Sharpe
    is_vs_oos_decay: float     # IS Sharpe - OOS Sharpe (overfitting measure)


def walk_forward(
    returns: np.ndarray,
    signal_func: Callable[[np.ndarray], np.ndarray],
    n_folds: int = 5,
    train_ratio: float = 0.6,
    config: BacktestConfig | None = None,
    periods_per_year: int = 252,
) -> WalkForwardResult:
    """Walk-forward validation with expanding or rolling window.

    Args:
        returns: full return series.
        signal_func: function(train_returns) → signals for the full series.
            Called on the training window, produces signals for the test window.
        n_folds: number of train/test splits.
        train_ratio: fraction of each fold used for training.

        result = walk_forward(returns, my_signal_func, n_folds=5)
        print(result.oos_sharpe)
    """
    cfg = config or BacktestConfig()
    n = len(returns)
    fold_size = n // n_folds

    is_sharpes = []
    oos_sharpes = []
    is_returns = []
    oos_returns = []

    for fold in range(n_folds):
        start = fold * fold_size
        end = min(start + fold_size, n)
        split = start + int((end - start) * train_ratio)

        train = returns[start:split]
        test = returns[split:end]

        if len(train) < 10 or len(test) < 5:
            continue

        # Generate signals from training data
        train_signals = signal_func(train)

        # Apply to test period
        test_signals = signal_func(test)

        # Compute metrics
        is_result = run_backtest(train, train_signals, cfg, periods_per_year)
        oos_result = run_backtest(test, test_signals, cfg, periods_per_year)

        is_sharpes.append(is_result.metrics.sharpe)
        oos_sharpes.append(oos_result.metrics.sharpe)
        is_returns.append(is_result.metrics.total_return)
        oos_returns.append(oos_result.metrics.total_return)

    avg_oos = float(np.mean(oos_sharpes)) if oos_sharpes else 0.0
    avg_is = float(np.mean(is_sharpes)) if is_sharpes else 0.0

    return WalkForwardResult(
        in_sample_sharpes=is_sharpes,
        out_of_sample_sharpes=oos_sharpes,
        in_sample_returns=is_returns,
        out_of_sample_returns=oos_returns,
        n_folds=len(is_sharpes),
        oos_sharpe=avg_oos,
        is_vs_oos_decay=avg_is - avg_oos,
    )


