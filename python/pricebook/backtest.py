# Signal combination & hypothesis testing
# ============================================================================

def combine_signals(
    signals: list[np.ndarray],
    weights: list[float] | None = None,
    method: str = "weighted",
) -> np.ndarray:
    """Combine multiple signal arrays into one.

    Args:
        signals: list of signal arrays (all same length).
        weights: per-signal weights. If None, equal weight.
        method: "weighted" (linear), "rank" (rank-average), "majority" (sign vote).
    """
    n = min(len(s) for s in signals)
    trimmed = [s[:n] for s in signals]

    if weights is None:
        weights = [1.0 / len(signals)] * len(signals)

    if method == "weighted":
        combined = sum(w * s for w, s in zip(weights, trimmed))
    elif method == "rank":
        # Rank each signal, then average ranks
        from scipy.stats import rankdata
        ranks = [rankdata(s) / n for s in trimmed]
        combined = sum(w * r for w, r in zip(weights, ranks))
    elif method == "majority":
        signs = [np.sign(s) for s in trimmed]
        combined = sum(w * sg for w, sg in zip(weights, signs))
    else:
        raise ValueError(f"Unknown method: {method}")

    return np.asarray(combined)


def deflated_sharpe(
    sharpe: float,
    n_trials: int,
    n_observations: int,
    skew: float = 0.0,
    kurtosis: float = 3.0,
) -> float:
    """Deflated Sharpe Ratio (Bailey & De Prado, 2014).

    Adjusts the Sharpe ratio for multiple testing (data snooping).
    Returns the probability that the observed Sharpe is significant
    given the number of strategies tested.

    DSR = Prob(SR > SR*) where SR* = expected max SR from n_trials random strategies.

    Args:
        sharpe: observed Sharpe ratio.
        n_trials: number of strategies tested (backtest count).
        n_observations: number of return observations.
        skew: return skewness (0 for normal).
        kurtosis: return kurtosis (3 for normal).
    """
    from scipy.stats import norm

    if n_trials <= 1:
        return 1.0

    # Expected maximum Sharpe from n random trials
    e_max_sr = norm.ppf(1 - 1 / n_trials)

    # Standard error of SR estimate
    se_sr = math.sqrt((1 + 0.5 * sharpe**2 - skew * sharpe +
                        (kurtosis - 3) / 4 * sharpe**2) / n_observations)

    if se_sr < 1e-10:
        return 1.0 if sharpe > e_max_sr else 0.0

    # Probability that observed SR exceeds expected max
    return float(norm.cdf((sharpe - e_max_sr) / se_sr))


def bonferroni_threshold(
    significance: float,
    n_tests: int,
) -> float:
    """Bonferroni-corrected significance threshold.

    For N independent tests at significance α, use α/N per test.
    """
    if n_tests <= 0:
        return significance
    return significance / n_tests


def fdr_threshold(
    p_values: list[float],
    significance: float = 0.05,
) -> float:
    """Benjamini-Hochberg FDR threshold.

    More powerful than Bonferroni for multiple testing correction.
    Returns the p-value threshold below which tests are significant.
    """
    sorted_p = sorted(p_values)
    n = len(sorted_p)
    threshold = 0.0
    for i, p in enumerate(sorted_p):
        bh = significance * (i + 1) / n
        if p <= bh:
            threshold = p
    return threshold
